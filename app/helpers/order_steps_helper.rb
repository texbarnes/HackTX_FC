module OrderStepsHelper
    
    def getShipping(quantity)
        if(quantity <= 20)
            @shipping = 4.99
        elsif(quantity <= 40)
            @shipping = 6.99
        else
            @shipping = 9.99
        end
    end
    
    def getSubtotal(orderAmount, shipping)
        @subtotal = orderAmount + shipping
        @subtotal = @subtotal.round(2)
    end
    
    def getTax(orderAmount, shipping)
        @tax = "0.00"
    end
    
    def getTotal(orderAmount, shipping, tax)
        @total = orderAmount + shipping + tax
        @total = @total.round(2)
    end 
    
